function dartList(list) {
  return `const [${(list || []).map(v => JSON.stringify(v)).join(', ')}]`.replace(/"/g, '\'');
}
function dartStringLiteral(v) {
  return `'${String(v).replace(/'/g, "\\'")}'`;
}
function dartStringOrNull(v) {
  if (v === null || v === undefined) return 'null';
  return dartStringLiteral(v);
}
function boolOr(value, fallback) {
  return value === true ? 'true' : value === false ? 'false' : (fallback ? 'true' : 'false');
}
function dartMap(obj) {
  const entries = Object.entries(obj || {});
  if (!entries.length) return 'const {}';
  const body = entries
    .filter(([k, v]) => k !== undefined && k !== null && v !== undefined && v !== null)
    .map(([k, v]) => `${dartStringLiteral(String(k))}: ${dartStringLiteral(String(v))}`)
    .join(', ');
  return `const {${body}}`;
}
function relMode(value) {
  const v = String(value || '').toLowerCase();
  return v === 'fullobject' ? 'RelationshipPayloadMode.fullObject' : 'RelationshipPayloadMode.idOnly';
}

function themeLiteral(theme) {
  const safeTheme = theme || {};
  const light = safeTheme.light || {};
  const dark = safeTheme.dark || {};
  const pick = (value, fallback) => (value !== undefined && value !== null ? value : fallback);
  const lp = pick(light.primary, '0xFF2D6CDF');
  const ls = pick(light.secondary, lp);
  const la = pick(light.accent, lp);
  const dp = pick(dark.primary, lp);
  const ds = pick(dark.secondary, ls);
  const da = pick(dark.accent, la);

  return `ThemeConfig(
      light: ThemePalette(
        primary: ${lp},
        secondary: ${ls},
        accent: ${la},
      ),
      dark: ThemePalette(
        primary: ${dp},
        secondary: ${ds},
        accent: ${da},
      ),
    )`;
}

